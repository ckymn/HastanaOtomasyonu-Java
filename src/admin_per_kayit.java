        stmt = conn.createStatement();
       
        //ekrana ne yazilacagini belirtir   
        JOptionPane.showMessageDialog(rootPane, sorgu);
       
        //guncelleme varsa eklenen degerler veya silinen degeler varsa devam ettir
        stmt.executeUpdate(sorgu);
            
        conn.close();
        
        
