    private void listeleme(){
     
      try
        {
          try {
              baglan();
          } catch (ClassNotFoundException ex) {
              Logger.getLogger(admin_per_kayit.class.getName()).log(Level.SEVERE, null, ex);
          }
                
        String sorgu = "select * from brans";
       
        stmt= conn.createStatement();
        
        rs=stmt.executeQuery(sorgu);
        
        rs.last();
        
        int ks = rs.getRow();
       
        String kss=""+ks;
        
        if(ks>0)
        {
            rs.beforeFirst();
        //rs.getString("Ad");
            while (rs.next()) {
                //eger hala sorgu devam ediyorsa brans adi ekle
               cmb_brans.addItem(rs.getString("brans_adi"));
            }
               }
        else
            //eger kayitli bir brans yoksa Kayitli bir servis yok densin
            JOptionPane.showConfirmDialog(null,"Kayitli Servis Yok");
        }
        catch (SQLException ex)
        {
         Logger.getLogger(admin_per_kayit.class.getName()).log(Level.SEVERE,null,ex);
       
            JOptionPane.showConfirmDialog(null,"hata"); //eger veritabanina baglanmaz ise 
        }   
        
        
        
     }
    
